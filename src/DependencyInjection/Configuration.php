<?php

declare(strict_types=1);

namespace Nowo\VaultBundle\DependencyInjection;

use Nowo\VaultBundle\Database\DatabaseDriver;
use Symfony\Component\Config\Definition\Builder\TreeBuilder;
use Symfony\Component\Config\Definition\ConfigurationInterface;

/**
 * Configuration tree for VaultBundle.
 */
final class Configuration implements ConfigurationInterface
{
    public const ALIAS = 'nowo_vault';

    public function getConfigTreeBuilder(): TreeBuilder
    {
        $treeBuilder = new TreeBuilder(self::ALIAS);
        $root        = $treeBuilder->getRootNode();

        $root
            ->children()
                ->scalarNode('user_class')
                    ->info('FQCN of the application user entity (must implement UserInterface and expose getId()).')
                    ->isRequired()
                    ->cannotBeEmpty()
                    ->example('App\\Entity\\User')
                ->end()
                ->scalarNode('table_prefix')
                    ->info('Prefix for vault tables (items, folders, grants).')
                    ->defaultValue('vault_')
                    ->cannotBeEmpty()
                ->end()
                ->scalarNode('encryption_key')
                    ->info('Base64-encoded 32-byte libsodium key for server-side payload encryption.')
                    ->isRequired()
                    ->cannotBeEmpty()
                ->end()
                ->integerNode('max_attachment_bytes')
                    ->info('Maximum document attachment size in bytes (stored encrypted in item payload).')
                    ->defaultValue(512_000)
                    ->min(0)
                ->end()
                ->arrayNode('password_field')
                    ->info('Login password field options when nowo-tech/password-strength-bundle is installed.')
                    ->addDefaultsIfNotSet()
                    ->children()
                        ->scalarNode('level')
                            ->defaultValue('medium')
                        ->end()
                        ->enumNode('generator_mode')
                            ->values(['off', 'input', 'modal'])
                            ->defaultValue('input')
                        ->end()
                        ->booleanNode('use_password_toggle')
                            ->defaultTrue()
                        ->end()
                    ->end()
                ->end()
                ->scalarNode('team_membership_resolver')
                    ->info('Service id implementing VaultTeamMembershipResolverInterface. Null uses no teams.')
                    ->defaultNull()
                ->end()
                ->arrayNode('database')
                    ->addDefaultsIfNotSet()
                    ->children()
                        ->enumNode('driver')
                            ->values(DatabaseDriver::drivers())
                            ->defaultValue(DatabaseDriver::DOCTRINE_ORM)
                        ->end()
                        ->enumNode('platform')
                            ->values(DatabaseDriver::platforms())
                            ->defaultValue(DatabaseDriver::PLATFORM_POSTGRESQL)
                        ->end()
                        ->scalarNode('entity_manager')
                            ->defaultValue('default')
                        ->end()
                    ->end()
                ->end()
                ->scalarNode('route_prefix')
                    ->defaultValue('')
                ->end()
                ->scalarNode('dashboard_route')
                    ->defaultNull()
                ->end()
                ->arrayNode('security')
                    ->addDefaultsIfNotSet()
                    ->children()
                        ->scalarNode('access_checker')
                            ->defaultNull()
                        ->end()
                        ->arrayNode('admin_roles')
                            ->scalarPrototype()->end()
                            ->defaultValue(['ROLE_ADMIN'])
                        ->end()
                        ->arrayNode('access_roles')
                            ->scalarPrototype()->end()
                            ->defaultValue(['ROLE_USER'])
                        ->end()
                        ->arrayNode('create_roles')
                            ->scalarPrototype()->end()
                            ->defaultValue(['ROLE_USER'])
                        ->end()
                        ->arrayNode('list_roles')
                            ->scalarPrototype()->end()
                            ->defaultValue(['ROLE_USER'])
                        ->end()
                        ->arrayNode('delete_roles')
                            ->scalarPrototype()->end()
                            ->defaultValue(['ROLE_USER'])
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('routes')
                    ->addDefaultsIfNotSet()
                    ->children()
                        ->arrayNode('index')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_index')->end()
                            ->end()
                        ->end()
                        ->arrayNode('items')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/items')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_items')->end()
                            ->end()
                        ->end()
                        ->arrayNode('shared')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/shared')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_shared')->end()
                            ->end()
                        ->end()
                        ->arrayNode('trash')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/trash')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_trash')->end()
                            ->end()
                        ->end()
                        ->arrayNode('item_new')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/items/new/{type}')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_item_new')->end()
                            ->end()
                        ->end()
                        ->arrayNode('item_edit')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/items/{id}/edit')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_item_edit')->end()
                            ->end()
                        ->end()
                        ->arrayNode('item_trash')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/items/{id}/trash')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_item_trash')->end()
                            ->end()
                        ->end()
                        ->arrayNode('item_restore')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/items/{id}/restore')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_item_restore')->end()
                            ->end()
                        ->end()
                        ->arrayNode('item_purge')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/items/{id}/purge')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_item_purge')->end()
                            ->end()
                        ->end()
                        ->arrayNode('folder_create')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/folders')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_folder_create')->end()
                            ->end()
                        ->end()
                        ->arrayNode('folder_trash')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/folders/{id}/trash')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_folder_trash')->end()
                            ->end()
                        ->end()
                        ->arrayNode('item_share')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/items/{id}/share')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_item_share')->end()
                            ->end()
                        ->end()
                        ->arrayNode('item_grant_revoke')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/items/{id}/grants/{grantId}/revoke')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_item_grant_revoke')->end()
                            ->end()
                        ->end()
                        ->arrayNode('folder_share')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/folders/{id}/share')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_folder_share')->end()
                            ->end()
                        ->end()
                        ->arrayNode('folder_grant_revoke')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/folders/{id}/grants/{grantId}/revoke')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_folder_grant_revoke')->end()
                            ->end()
                        ->end()
                        ->arrayNode('password_generate')
                            ->addDefaultsIfNotSet()
                            ->children()
                                ->scalarNode('path')->defaultValue('/tools/vault/password/generate')->end()
                                ->scalarNode('name')->defaultValue('nowo_vault_password_generate')->end()
                            ->end()
                        ->end()
                    ->end()
                ->end()
                ->arrayNode('templates')
                    ->addDefaultsIfNotSet()
                    ->children()
                        ->scalarNode('layout')
                            ->defaultValue('@NowoVaultBundle/layout.html.twig')
                        ->end()
                        ->scalarNode('home')
                            ->defaultValue('@NowoVaultBundle/vault/home.html.twig')
                        ->end()
                        ->scalarNode('items')
                            ->defaultValue('@NowoVaultBundle/vault/items.html.twig')
                        ->end()
                        ->scalarNode('index')
                            ->info('Deprecated alias of home. Use home instead.')
                            ->defaultValue('@NowoVaultBundle/vault/home.html.twig')
                        ->end()
                        ->scalarNode('item_form')
                            ->defaultValue('@NowoVaultBundle/vault/item_form.html.twig')
                        ->end()
                        ->scalarNode('trash')
                            ->defaultValue('@NowoVaultBundle/vault/trash.html.twig')
                        ->end()
                        ->scalarNode('shared')
                            ->defaultValue('@NowoVaultBundle/vault/shared.html.twig')
                        ->end()
                        ->scalarNode('share')
                            ->defaultValue('@NowoVaultBundle/vault/share.html.twig')
                        ->end()
                    ->end()
                ->end()
                ->scalarNode('firewall')
                    ->defaultValue('main')
                ->end()
            ->end();

        return $treeBuilder;
    }
}
